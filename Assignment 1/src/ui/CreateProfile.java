    private void submitBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitBtnActionPerformed
	//Form validation for mandatory fields
	if(!isFormValid()) {
		return;
	}
	
	DeveloperProfile dev = new DeveloperProfile();
	
	// Personal Information
	dev.setFirstName(firstName.getText());
	dev.setLastName(lastName.getText());
	dev.setUserName(username.getText());
	dev.setEmail(email.getText());
	dev.setPhone(phone.getText());
	dev.setDateOfBirth(dob.getDate());
	
	// Identification details
	dev.setDriversLicenseNumber(driverLicenseNo.getText());
	dev.setDriversLicenseState(driverLicenseState.getText());
	dev.setSocialSecurityNumber(ssn.getText());
	
	//Address Details
	dev.setAddressLine1(addressLine1.getText());
	dev.setAddressLine2(addressLine2.getText());
	dev.setCity(city.getText());
	dev.setState(state.getText());
	dev.setZip(zipCode.getText());
	
	//Contact Information
	dev.setTelephoneNumber(telephoneNo.getText());
	dev.setFaxNumber(faxNo.getText());
	
	//Professional Information
	dev.setLinkedInProfile(linkedin.getText());
	dev.setYearsOfExperience((int) yearsOfExp.getValue());
	dev.setTechnicalSkills(technicalSkills.getSelectedValuesList());
	dev.setTechnicalSkillsIndices(technicalSkills.getSelectedIndices());
	dev.setAreaOfInterest(areaOfInterest.getSelectedValuesList());
	dev.setAreaOfInterestIndices(areaOfInterest.getSelectedIndices());
	dev.setProfessionalSummary(professionalSummary.getText());
	
	developers.put(username.getText(), dev);
	
	JOptionPane.showMessageDialog(this, "Developer information saved successfully!", "Form Saved", JOptionPane.INFORMATION_MESSAGE);
	
	//Clear fields
	firstName.setText("");
	lastName.setText("");
	username.setText("");
	email.setText("");
	phone.setText("");
	dob.setDate(null);
	driverLicenseNo.setText("");
	driverLicenseState.setText("");
	ssn.setText("");
	addressLine1.setText("");
	addressLine2.setText("");
	city.setText("");
	state.setText("");
	zipCode.setText("");
	telephoneNo.setText("");
	faxNo.setText("");
	linkedin.setText("");
	yearsOfExp.setValue(0);
	technicalSkills.clearSelection();
	areaOfInterest.clearSelection();
	professionalSummary.setText("");
    }//GEN-LAST:event_submitBtnActionPerformed

