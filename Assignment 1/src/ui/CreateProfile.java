	public  boolean isFormValid() {
		if (isEmpty(firstName.getText())) {
			showMessage("First Name is mandatory.");
			return false;
		}
		if (isEmpty(lastName.getText())) {
			showMessage("Last Name is mandatory.");
			return false;
		}
		if (isEmpty(username.getText())) {
			showMessage("Username is mandatory.");
			return false;
		}
		if(developers.containsKey(username.getText())) {
			showMessage("Username is already taken, please choose a new username!");
			return false;
		}
		if (isEmpty(email.getText())) {
			showMessage("Email is mandatory.");
			return false;
		}
		String emailPattern = "^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$";
		if(!email.getText().matches(emailPattern)) {
			showMessage("Enter a valid Email-ID");
			return false;
		}
		if (isEmpty(driverLicenseNo.getText())) {
			showMessage("Driver's License Number is mandatory.");
			return false;
		}
		if (isEmpty(driverLicenseState.getText())) {
			showMessage("Driver's License State is mandatory.");
			return false;
		}
		if (isEmpty(ssn.getText())) {
			showMessage("Social Security Number is mandatory.");
			return false;
		}
		if (isEmpty(linkedin.getText())) {
			showMessage("LinkedIn Profile is mandatory.");
			return false;
		}
		if (technicalSkills.getSelectedValuesList().isEmpty()) {
			showMessage("Technical Skills are mandatory.");
			return false;
		}
		if (areaOfInterest.getSelectedValuesList().isEmpty()) {
			showMessage("Area of Interest is mandatory.");
			return false;
		}

		// If all mandatory fields are filled
		return true;
		
	}

