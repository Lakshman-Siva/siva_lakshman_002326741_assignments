    private void viewSelectedPersonBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewSelectedPersonBtnActionPerformed
        // TODO add your handling code here:
	int selectedRow = peopleTable.getSelectedRow();
	
	if(selectedRow < 0) {
		JOptionPane.showMessageDialog(null,  "Please select a person from the list",  "Warning",  JOptionPane.WARNING_MESSAGE);
		return;
	}
	
	Person selectedAccount = (Person) peopleTable.getValueAt(selectedRow,  0);
	
	ViewPerson panel = new ViewPerson(mainFramePanel, personDirectory, selectedAccount);
	mainFramePanel.add("ViewAccountJPanel", panel);
	
	CardLayout layout = (CardLayout) mainFramePanel.getLayout();
	layout.next(mainFramePanel);
    }//GEN-LAST:event_viewSelectedPersonBtnActionPerformed

    private void deleteSelectedPersonBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteSelectedPersonBtnActionPerformed
        // TODO add your handling code here:
	int selectedRow = peopleTable.getSelectedRow();
	
	if(selectedRow < 0) {
		JOptionPane.showMessageDialog(null,  "Please select a person from the list",  "Warning",  JOptionPane.WARNING_MESSAGE);
		return;
	}
	
	int dialogResult = JOptionPane.showConfirmDialog(null,  "Are you sure wante to delete the selected account?",  "Warning", JOptionPane.YES_NO_OPTION);
	if(dialogResult == JOptionPane.YES_OPTION) {
		Person selectedAccount = (Person) peopleTable.getValueAt(selectedRow,  0);
		personDirectory.removePerson(selectedAccount);
		populateTable();
	}
    }//GEN-LAST:event_deleteSelectedPersonBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
	String searchText = searchBox.getText();
	
	if(searchText.isBlank()) {
		JOptionPane.showMessageDialog(null,  "Please type the search text to view",  "Warning", JOptionPane.WARNING_MESSAGE);
		return;
	}
	
	Person person = personDirectory.searchPerson(searchText);
	if(person == null) {
		JOptionPane.showMessageDialog(null,  "No Person found for given search text. Please try again.",  "Warning", JOptionPane.WARNING_MESSAGE);
		return;
	}
	
	ViewPerson panel = new ViewPerson(mainFramePanel, personDirectory, person);
	mainFramePanel.add("ViewAccountJPanel", panel);
	CardLayout layout = (CardLayout) mainFramePanel.getLayout();
	layout.next(mainFramePanel);
	
	searchBox.setText("");
    }//GEN-LAST:event_searchBtnActionPerformed


