	public void populateTable() {
		DefaultTableModel model = (DefaultTableModel) peopleTable.getModel();
		model.setRowCount(0);
		
		for(Person person : personDirectory.getPersons()) {
			Object[] row = new Object[6];
			row[0] = person;
			row[1] = person.getLastName();
			row[2] = person.getHomeCity();
			row[3] = person.getHomeZipCode();
			row[4] = person.getWorkCity();
			row[5] = person.getWorkZipCode();
			
			model.addRow(row);
		}
	}

